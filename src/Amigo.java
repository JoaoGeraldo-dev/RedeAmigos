
public class Amigo {
	private String nome, sexo, mensagem;
	private int idade;
	
	
	public String getNome() {
		return nome;
	}
	
	public String getSexo() {
		return sexo;
	}
	
	public String getMensagem() {
		return mensagem;
	}
	
	public int getIdade() {
		return idade;
	}
	
	public void SetNome(String nome) {
		this.nome = nome;
	}
	
}
